## using turtle to draw a triangle
import turtle
# let's start up a Turtle Graphics window with a red turtle
turtle.color('green')
# put its pen down so all movement will show as a red line
turtle.pendown()
# move forward by 100 in current direction
turtle.forward(100)
# rotate direction of turtle by 60 degrees to the right
turtle.right(60)
# move forward by 100 in current direction
turtle.forward(100)
# rotate direction of turtle by 60 degrees to the right
turtle.right(60)
# move forward by 100 in current direction
turtle.forward(100)
# rotate direction of turtle by 60 degrees to the right
turtle.right(60)
# move forward by 100 in current direction
turtle.forward(100)
# rotate direction of turtle by 60 degrees to the right
turtle.right(60)
# move forward by 100 in current direction
turtle.forward(100)
# rotate direction of turtle by 60 degrees to the right
turtle.right(60)
# move forward by 100 in current direction
turtle.forward(100)
# make the turtle invisible
turtle.hideturtle()
# wait for user to click on the screen to exit
turtle.exitonclick()
